  <h2>Seriously cross-platform with HTML5</h2>

  <p class="seriously-cross-platform" style="width:60%;">jQuery mobile framework takes the "write less, do more" mantra to the next level: Instead of writing unique apps for each mobile device or OS, the jQuery mobile framework allows you to design a single highly-branded web site or application that will work on all popular smartphone, tablet, and desktop platforms. <a href="/gbs/" class="arrow">Device support</a></p>

